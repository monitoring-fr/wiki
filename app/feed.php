        <summary>1. Concepts clefs

La fonction principale d’OpenNMS est de recevoir des traps SNMP. Ces traps SNMP sont décodées à l’aide de fichiers de configuration créés à partir des MIBs SNMP. Les traps SNMP, une fois décodées, sont visibles sous forme d’évènement dans la vue Events et sous forme d’alarme dans la vue Alarms. Ces deux vues peuvent contenir également les évènements internes à OpenNMS.</summary>
